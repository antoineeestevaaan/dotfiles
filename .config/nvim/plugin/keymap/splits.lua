-- Maximize, close and equalize
vim.keymap.set("n", "<leader>wm", "<C-w>_<C-w>|")
vim.keymap.set("n", "<leader>wc", "<C-w>o")
vim.keymap.set("n", "<leader>we", "<C-w>=")

-- Focus
vim.keymap.set("n", "<leader>wh", "<C-w>h")
vim.keymap.set("n", "<leader>wj", "<C-w>j")
vim.keymap.set("n", "<leader>wk", "<C-w>k")
vim.keymap.set("n", "<leader>wl", "<C-w>l")

-- Move around
vim.keymap.set("n", "<leader>wH", "<C-w>H")
vim.keymap.set("n", "<leader>wJ", "<C-w>J")
vim.keymap.set("n", "<leader>wK", "<C-w>K")
vim.keymap.set("n", "<leader>wL", "<C-w>L")

-- Control the size
vim.keymap.set("n", "<leader>ww", "<c-w>5>")
vim.keymap.set("n", "<leader>wn", "<c-w>5<")
vim.keymap.set("n", "<leader>wt", "<C-W>+")
vim.keymap.set("n", "<leader>ws", "<C-W>-")
